<template>
  <div>
    <p>
      <button v-on:click="add()" class="btn btn-white btn-default btn-round">
        <i class="ace-icon fa fa-edit"></i>
        新增
      </button>
      &nbsp;
      <button v-on:click="list(1)" class="btn btn-white btn-default btn-round">
        <i class="ace-icon fa fa-refresh"></i>
        刷新
      </button>
    </p>
    <pagination ref="pagination" v-bind:list="list" v-bind:itemCount="8"></pagination>
    <table id="simple-table" class="table  table-bordered table-hover">
      <thead>
      <tr>
         <th>id</th>

         <th>课程目标</th>

         <th>老师评价</th>

         <th>学生评价</th>

         <th>目标达成评价值</th>
        <th>操作</th>
      </tr>

      </thead>

      <tbody>
      <tr v-for="courseTarget in courseTargets">
              <td>{{courseTarget.id}}</td>
              <td>{{courseTarget.target}}</td>
              <td>{{courseTarget.teacherEvaluate}}</td>
              <td>{{courseTarget.studentEvaluate}}</td>
              <td>{{courseTarget.goalScore}}</td>
        <td>

          <div class="hidden-sm hidden-xs btn-group">
            <button v-on:click="edit(courseTarget)" class="btn btn-xs btn-info">
              <i class="ace-icon fa fa-pencil bigger-120"></i>
            </button>

            <button v-on:click="del(courseTarget.id)" class="btn btn-xs btn-danger">
              <i class="ace-icon fa fa-trash-o bigger-120"></i>
            </button>
          </div>
        </td>
      </tr>
      <tr>
        <th>整体课程目标</th>

        <th></th>

        <th>{{courseTargetLow.teacherEvaluate}}</th>
        <th>{{}}</th>
        <th>{{courseTargetLow.goalGrade}}</th>
      </tr>
      </tbody>
    </table>
    <div id="form-modal" class="modal fade" tabindex="-1" role="dialog">
      <div class="modal-dialog" role="document">
        <div class="modal-content">
          <div class="modal-header">
            <button type="button" class="close" data-dismiss="modal" aria-label="Close"><span
                aria-hidden="true">&times;</span></button>
            <h4 class="modal-title">表单</h4>
          </div>
          <div class="modal-body">
            <form class="form-horizontal">
                <div class="form-group">
                  <label class="col-sm-2 control-label">课程目标</label>
                  <div class="col-sm-10">
                    <input v-model="courseTarget.target" class="form-control">
                  </div>
                </div>
                <div class="form-group">
                  <label class="col-sm-2 control-label">老师评价</label>
                  <div class="col-sm-10">
                    <input v-model="courseTarget.teacherEvaluate" class="form-control">
                  </div>
                </div>
            </form>
          </div>
          <div class="modal-footer">
            <button type="button" class="btn btn-default" data-dismiss="modal">取消</button>
            <button v-on:click="save()" type="button" class="btn btn-primary">保存</button>
          </div>
        </div><!-- /.modal-content -->
      </div><!-- /.modal-dialog -->
    </div><!-- /.modal -->
  </div>
</template>

<script>
import Pagination from "../../components/pagination";

export default {
  name: "business-courseTarget",
  components: {Pagination},
  data: function () {
    return {
      courseTarget: {},
      courseTargets: [],
      courseTargetLows:[],
      courseTargetLow:{},
    }
  },
  mounted() {
    let _this = this;
    _this.$refs.pagination.size = 1000;
    _this.list(1);

    //sidebar激活样式方法一
    // this.$parent.activeSidebar("business-courseTarget-sidebar");
  },
  methods: {
    add() {
      //这个add的作用就是点开那个模态框，新增是save
      let _this = this;
      _this.courseTarget = {}
      $("#form-modal").modal('show');
    },
    edit(courseTarget) {
      //这个add的作用就是点开那个模态框，新增是save
      let _this = this;
      _this.courseTarget = $.extend({}, courseTarget);
      $("#form-modal").modal('show');
    },
    list(page) {
      let _this = this;
      _this.$ajax.post(process.env.VUE_APP_SERVER + "/business/admin/courseTarget/list",
          {
            page: page,
            size: _this.$refs.pagination.size
          }).then((response) => {
        console.log("查询课程目标列表结果", response);
        let resp = response.data;
        _this.courseTargets = resp.content.list;
        _this.$ajax.post(process.env.VUE_APP_SERVER + "/business/admin/courseTargetLow/list",
            {
              page: page,
              size: _this.$refs.pagination.size
            }).then((response) => {
          console.log("查询平均分列表结果", response);
          let resp = response.data;
          _this.courseTargetLows = resp.content.list;
          _this.courseTargetLow = _this.courseTargetLows[0];

          console.log("courseTargetLows",_this.courseTargetLows[0]);
        })

        _this.$refs.pagination.render(page, resp.content.total);
      })
    },
    save() {
      let _this = this;
        // 保存校验
        if (1 != 1
          || !Validator.length(_this.courseTarget.target, "课程目标", 1, 200)
        ) {
          return;
        }

      _this.$ajax.post(process.env.VUE_APP_SERVER + "/business/admin/courseTarget/save",
          _this.courseTarget).then((response) => {
        console.log("查询课程目标列表结果", response);
        let resp = response.data;
        if (resp.success) {
          $("#form-modal").modal('hide');
          _this.list(1)
          Toast.success("保存成功");
        }else{
          Toast.warning(resp.message);

        }
      })
    },
    del(id) {
      let _this = this;
      Confirm.show("删除课程目标后不可恢复!",function () {
        Loading.show();
        _this.$ajax.post(process.env.VUE_APP_SERVER + "/business/admin/courseTarget/delete/" + id).then((response) => {
          console.log("删除课程目标列表结果", response);
          let resp = response.data;
          if (resp.success) {
            Toast.success("删除成功");
            _this.list(1);

          }
        })
    });
    }

  }
}
</script>